const ImWchar* ui_cjk_ranges() {
    static std::vector<ImWchar> r;
    if (r.empty()) {
        std::set<ImWchar> uniq(std::begin(kCjkChars), std::end(kCjkChars));
        for (ImWchar c : uniq) { r.push_back(c); r.push_back(c); }
        r.push_back(0);
    }
    return r.data();
}

